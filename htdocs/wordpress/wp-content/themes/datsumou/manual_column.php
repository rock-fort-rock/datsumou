		<p>星マークを入れたい場合は、Post Snipepets->「星マーク」を選択後、numに表示数（半角数字）を入力してください。<br>
		※表の中に限らず、どこでも使えます。</p>
	</div>
	<div class="card">
		<h2>吹き出し</h2>
		<p>Post Snipepets->「吹き出し」から基本コードを呼び出せます。</p>
		<p>デザインの変更方法は<a href="https://queseraselife.com/speechbubble/#toc3" target="_blank">こちら</a>をご覧ください。</p>
		<p>アバターを変更したい場合は/wordpress/wp-content/plugins/speech-bubble/img/へFTPでファイルをアップする必要があります。</p>
	</div>
	<div class="card">
		<h2>ボタン</h2>
		<p>Post Snipepets->「ボタン」を選択後、「link」にリンク先URL、「color」にgreen、blue、red、orangeのいずれか、「type」に拡大するタイプならexpand、点滅するタイプならblinkを、「text」にボタン内のテキストを入力してください。</p>
	</div>
	<div class="card">
		<h2>リスト</h2>
		<p>Post Snipepets->「リスト」を選択後、「title」にタイトル（任意）、「text」にカンマ「,」区切りでリスト項目のテキストを入力してください。</p>
	</div>
	<div class="card">
		<h2>ランキング装飾</h2>
		<p>Post Snipepets->「ランキング装飾」を選択後、「rank」に数字（半角数字）、「title」にタイトル、「catch」にタイトルの下に入るテキスト（任意）を入力してください。</p>
	</div>
	<div class="card">
